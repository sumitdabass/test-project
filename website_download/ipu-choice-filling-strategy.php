<h2>Freeze vs Float — Understanding Counselling Options</h2>

<h3>Freeze</h3>

<ul>
<li>Accept current allotted seat permanently.</li>
<li>No participation in next rounds.</li>
<li>Use when satisfied with college + course.</li>
</ul>

<h3>Float</h3>

<ul>
<li>Keep current seat but try for upgrade.</li>
<li>System auto-upgrades if better option available.</li>
<li>Recommended when expecting higher preference.</li>
</ul>

