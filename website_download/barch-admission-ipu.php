  <h2>B.Arch Curriculum at IPU &ndash; What You Will Study</h2>
  <p>The 5-year B.Arch programme at IPU is divided into 10 semesters. The curriculum covers:</p>
  <ul>
    <li><strong>Architectural Design:</strong> The core of the programme &mdash; design studios where you develop projects from concept to detailed drawings, progressing from small structures to complex buildings and urban spaces.</li>
    <li><strong>Building Construction &amp; Materials:</strong> Study construction techniques, building materials, structural systems, plumbing, electrical systems, and sustainable construction practices.</li>
    <li><strong>Structural Engineering:</strong> Mechanics, structural analysis, and design of RCC, steel, and timber structures.</li>
    <li><strong>History of Architecture:</strong> Study architectural heritage from ancient civilisations to modern movements including Indian, Islamic, Gothic, Renaissance, and contemporary architecture.</li>
    <li><strong>Urban Planning &amp; Landscape:</strong> Town planning, urban design, landscape architecture, and housing.</li>
    <li><strong>Building Services:</strong> HVAC, acoustics, lighting, fire safety, and green building technologies.</li>
    <li><strong>Computer-Aided Design:</strong> AutoCAD, Revit, SketchUp, 3ds Max, and BIM (Building Information Modelling).</li>
    <li><strong>Professional Practice:</strong> Architectural firm management, contracts, building bylaws, and professional ethics.</li>
    <li><strong>Thesis &amp; internship:</strong> Final year includes a thesis project and mandatory practical training at an architecture firm.</li>
  </ul>

  <h2>Career Scope After B.Arch from IPU</h2>
  <p>B.Arch graduates from IPU have diverse career opportunities:</p>
  <ul>
    <li><strong>Licensed architect:</strong> Register with the Council of Architecture and practise as a professional architect &mdash; design buildings, homes, offices, and public spaces.</li>
    <li><strong>Urban planner:</strong> Work with municipal corporations, smart city projects, and urban development authorities on city planning and infrastructure.</li>
    <li><strong>Interior designer:</strong> Specialise in interior design for residential, commercial, or hospitality projects.</li>
    <li><strong>Construction management:</strong> Oversee building projects as a project manager at construction firms and real estate companies.</li>
    <li><strong>Landscape architect:</strong> Design outdoor spaces, parks, gardens, and campus landscapes.</li>
    <li><strong>Higher studies:</strong> Pursue M.Arch, M.Plan, or MBA in Construction Management from top universities in India or abroad.</li>
    <li><strong>Government jobs:</strong> Roles in CPWD, state PWDs, NBCC, DDA, and other government agencies.</li>
    <li><strong>Typical salary:</strong> Starting packages for B.Arch graduates range from 4&ndash;8 LPA, with experienced architects earning significantly more.</li>
  </ul>

