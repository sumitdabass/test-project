    <li><strong>Examination Fee:</strong> Rs. 3,000 per year</li>
    <li><strong>Development / Innovation Fee:</strong> Rs. 500 - Rs. 10,000 per year (varies by college)</li>
    <li><strong>Alumni Contribution:</strong> Rs. 2,000 (one-time, at admission)</li>
    <li><strong>Caution Money:</strong> Rs. 5,000 - Rs. 10,000 (refundable at the end of the course)</li>
    <li><strong>Hostel Fee (if applicable):</strong> Rs. 70,000 - Rs. 1,20,000 per year (only at colleges with hostels)</li>
