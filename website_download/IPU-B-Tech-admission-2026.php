<h2>IPU B.Tech Eligibility Criteria (Latest)</h2>

<ul>
<li>Minimum <strong>55% marks in Class 12</strong> with Physics, Chemistry & Mathematics.</li>
<li><strong>First preference:</strong> JEE Main score (primary admission criteria).</li>
<li><strong>Second pathway:</strong> CUET score (subject to official admission notification).</li>
<li>Participation in centralized GGSIPU counselling is mandatory.</li>
