  <h2>IPU Counselling Rounds Explained</h2>
  <ul>
    <li><strong>Round 1:</strong> First seat allotment based on choice filling. You can Freeze, Float, or Withdraw your seat.</li>
    <li><strong>Round 2:</strong> Vacant seats from Round 1 are re-allotted to candidates who chose "Float" or new candidates with lower ranks.</li>
    <li><strong>Round 3 / Sliding Round:</strong> Internal sliding to upgrade seats based on remaining vacancies. Existing students can get a better branch/college without losing their current seat.</li>
    <li><strong>Spot / Physical Round:</strong> The last chance for unfilled seats. Conducted offline at the campus. Open to candidates who registered earlier but didn't get a seat.</li>
  </ul>

