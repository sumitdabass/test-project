<h2>JIMS Admission Process 2026</h2>

<p>Admission to IPU-affiliated programmes (BBA, BCA, MBA) at JIMS is conducted through the centralised GGSIPU counselling process. Candidates need valid IPU CET or CUET scores for undergraduate programmes and CAT, MAT, or IPU CET scores for the MBA programme. Registration on the GGSIPU counselling portal and careful preference filling are essential steps.</p>

<p>For the PGDM programme, admission is managed directly by JIMS. Candidates with valid scores in CAT, MAT, XAT, or GMAT can apply through the institute's own admission process, which includes a written ability test, group discussion, and personal interview. For detailed admission assistance for any JIMS programme, call 9899991342.</p>

